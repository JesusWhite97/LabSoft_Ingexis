    class regLogReg{
        public function Entrada($CorreoUser, $accion){
            //crea Conexion===============
            $conex = new conexionMySQLi();
            $mysqli = $conex->conexion("Registrador");
            //============================
            mysqli_query($mysqli, "SET NAMES 'utf8'");
            $query = "CALL log_reg('".$CorreoUser."', '".$accion."')";
            if($mysqli->query($query)===TRUE){
                return 'true';
            }else{
                return "Error: ".$mysqli->error;
            }    
        }
    }
